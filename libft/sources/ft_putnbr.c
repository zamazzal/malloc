#include "libft.h"

void	ft_putnbr(int n)
{
	if ((n < 0) && (n > -2147483648))
	{
		ft_putchar('-');
		ft_putnbr(n * -1);
	}
	else if ((n >= 0) && (n <= 9))
	{
		ft_putchar(n + '0');
	}
	else if ((n > 9) && (n <= 2147483647))
	{
		ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
	else if (n == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		ft_putnbr(147483648);
	}
}
