#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t i;

	i = 0;
	while ((src[i] != '\0') && (i < len))
	{
		dst[i] = (unsigned char)src[i];
		i++;
	}
	ft_bzero(&dst[i], len - i);
	return (dst);
}
