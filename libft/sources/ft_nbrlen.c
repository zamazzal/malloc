#include "libft.h"

size_t		ft_nbrlen(int nb)
{
	int		size;

	size = (nb <= 0) ? 1 : 0;
	nb = (nb < 0) ? nb * -1 : nb;
	while (nb)
	{
		nb = nb / 10;
		size++;
	}
	return (size);
}
